package Homework3_3;

public class Homework3_3Task1 {
    public static void main(String[] args) {

        for (int i = 10; i <= 20; i++) {
            System.out.println(i);
        }
        int i = 10;
        while (i <= 20) {
            System.out.println(i);
            i++;

        }

        i = 10;
        do {
            System.out.println(i);
            i++;
        }
        while (i <= 20);

    }
}

